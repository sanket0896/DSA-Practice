/**
 * Repeating Sub-Sequence
Problem Description
Given a string A, find if there is any subsequence that repeats itself. A subsequence of a string is defined as a sequence of characters generated by deleting some characters in the string without changing the order of the remaining characters. NOTE: Sub-sequence length should be greater than or equal to 2. 


Problem Constraints
1 <= length(A) <= 100


Input Format
The first and the only argument of input contains a string A.


Output Format
Return an integer, 1 if there is any subsequence which repeat itself else return 0.


Example Input
Input 1:
 A = "abab"
Input 2:
 A = "abba"


Example Output
Output 1:
 1
Output 2:
 0


Example Explanation
Explanation 1:
 "ab" is repeated.
Explanation 2:
 There is no repeating subsequence.
 */

function solve(A) {
    A = A.split("")
    let al = A.length;
    let ans = false;
    let freq = new Map();

    for(let i = 0; i < al; ++i){
        if(freq.has(A[i])){
            let f = freq.get(A[i]);
            ++f;
            freq.set(A[i], f);

            if(f > 2){
                ans = true;
                break;
            }
        }else{
            freq.set(A[i], 1);
        }
    }

    if(ans){
        return 1;
    }

    for(let i = 0; i < al; ++i){
        if(freq.get(A[i]) === 1){
            A[i] = '';
        }
    }

    A = A.join('');
    al = A.length;

    if(al === 2){
        return 0;
    }

    let i = 0;
    let j = al - 1;

    while(i < j){
        if(A[i] !== A[j]){
            ans = true;
            break;
        }
        ++i;
        --j;
    }

    return ans ? 1 : 0;
}

A = 'yeiajkslbpaqnms';
console.log(solve(A));
